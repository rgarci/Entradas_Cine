    private void btnAceptarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAceptarMouseClicked
        ArrayList<Butaca> arraux = new ArrayList();
        //agrego la lista de butacas que se han seleccionados para ocuparlas
        for(int n=0;n<FUNCION.sele.getButacas().size();n++){
            arraux.add(FUNCION.sele.getButacas().get(n));
        }
        for(int m=0; m<FUNCION.buts.size();m++){
            arraux.add(FUNCION.buts.get(m));
        }
        //buscala la funcion para removerla y agregar la que tiene las butacas ocupadas
        //y asi que ya no esten disponibles
        FUNCION.sele.setButacas(arraux);
         for(int i=0;i<FUNCION.pels.size();i++){
            if( FUNCION.pels.get(i).equals(FUNCION.seleAux)){
                FUNCION.pels.remove(i);
                FUNCION.pels.add(FUNCION.sele);
            }
        }
        
        try{
            //metemos los nuevos datos de la funcion al archivo de texto
            FileOutputStream fs = new FileOutputStream("funciones.txt");
            ObjectOutputStream ob = new ObjectOutputStream(fs);
            ob.writeObject(FUNCION.pels);
        }
        catch(FileNotFoundException e1){
            JOptionPane.showMessageDialog(null, e1.getMessage());
        }
        catch(IOException e2){
            JOptionPane.showMessageDialog(null, e2.getMessage());
        }
        new TICKET().setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnAceptarMouseClicked

