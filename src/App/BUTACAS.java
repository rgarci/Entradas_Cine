    public void Ocupados(){
        //desactivo los botones de los lugares que ya han sido comprados, para no volverlos ha comprar
        //los asientos disponibles estan en color verde, los que el cliente elija 
        //se ponen en color amarillo y los que ya no se encuentren disponibles estan desactivados
        ArrayList<Butaca> butacas = FUNCION.sele.getButacas();
        if(!butacas.isEmpty()){
            for(int i=0; i<butacas.size();i++){
                botones[butacas.get(i).getFila()][butacas.get(i).getColumna()].setEnabled(false);
            }
        }
    }
    
    public void Visible(){
        //envio los botones al panel para poder visualizarlos
        for (int i=0;i<5;i++){
            for (int j=0; j<7;j++){
                mainpanel.add(botones[i][j]);
            }
        }
    }
    
    public void Botones (){
    //creo los botones de la clase Asiento
    for (int i = 0; i < 5; i++) {
        for (int j = 0; j < 7; j++) {
            botones[i][j] = new Asiento(50*j,50*i,50,50);
            botones[i][j].CambiarNombre(i, j);
            
        }
        
    }
}
