    private void jBGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBGuardarMouseClicked
        Cliente cli = new Cliente(jTNombre.getText(), jTMatricula.getText(), jTApellido.getText(), jDFec_Nacimiento.getDate());
        ArrayList<Cliente> clien = new ArrayList();
        int ban=0;
        try{
            FileInputStream fis = new FileInputStream("clientes.txt");
            ObjectInputStream ois = new ObjectInputStream(fis);

            clien = (ArrayList<Cliente>) ois.readObject();
            ois.close();
            for(int i=0;i<clien.size();i++){
                if(clien.get(i).getMat().equals(cli.getMat())){
                    ban=1;
                }
            }
            if(ban==0){
                clien.add(cli);
            }
            if(ban==1){
                throw new ExistentCuentaException("La cuenta ya existe");
            }
            FileOutputStream fs = new FileOutputStream("clientes.txt");
            ObjectOutputStream ob = new ObjectOutputStream(fs);
            ob.writeObject(clien);
            ob.close();
        }
        catch(FileNotFoundException e1){
            JOptionPane.showMessageDialog(null, e1.getMessage());
        }
        catch(IOException e2){
            JOptionPane.showMessageDialog(null,e2.getMessage());
        }
        catch(ClassNotFoundException e3){
            JOptionPane.showMessageDialog(null, e3.getMessage());
        }
        catch(ExistentCuentaException e4){
            JOptionPane.showMessageDialog(null, e4.getMessage());
        }
        this.dispose();
    }//GEN-LAST:event_jBGuardarMouseClicked

