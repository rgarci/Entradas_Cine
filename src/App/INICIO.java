        pelis.add(new Funcion4D("IT", "16:20", "C", "CC4"));
        pelis.add(new Funcion3D("IT", "16:20", "C", "CC3"));
        pelis.add(new FuncionNormal("Selena", "13:00", "A", "H5"));
        pelis.add(new FuncionNormal("Selena", "17:00", "A", "H5"));
        pelis.add(new FuncionNormal("Selena", "20:00", "A", "H5"));
        pelis.add(new Funcion4D("COCO", "13:00", "A", "C4"));
        pelis.add(new Funcion3D("COCO", "17:00", "A", "C3"));
        pelis.add(new FuncionNormal("COCO", "15:00", "A", "C1"));
        try{
            FileOutputStream fs = new FileOutputStream("funciones.txt");
            ObjectOutputStream ob = new ObjectOutputStream(fs);
            ob.writeObject(pelis);
        }
        catch(FileNotFoundException e1){
            JOptionPane.showMessageDialog(null, e1.getMessage());
        }
        catch(IOException e2){
            JOptionPane.showMessageDialog(null, e2.getMessage());
        }
        new Menu().setVisible(true);
        this.dispose();
